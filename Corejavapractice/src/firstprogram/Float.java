package firstprogram;

public class Float {

	public static void main(String[] args) {

		float Height = 5.3f;
		System.out.println("What is your height=+" + Height);

		float Weight = 59.3f;
		System.out.println("What is your weight=+" + Weight);

		// Area,circumference and Perimeter of circle

		int radius = 56;
		double area = 3.14 * radius * radius;
		System.out.println("The are of the circle=:" + area);

		float radius1 = 28.3f;
		double circumference = 2 * 3.14 * radius1;
		System.out.println("The circumference of the circle=:" + circumference);

		float radius2 = 2.80f;
		double perimeter = 3.14 * radius2 + 2 * radius2;
		System.out.println("The perimeter of the circle=:" + perimeter);

	}

}
