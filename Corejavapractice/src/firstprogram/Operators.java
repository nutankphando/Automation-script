package firstprogram;

public class Operators {

	public static void main(String[] args) {

		int a = 55;
		int b = 85;
		int c = 45;

		int sum = a + b + c;
		System.out.println(" The sum of total number:" + sum);

		float e = 45.5f;
		float g = 55.5f;
		float h = 70.5f;

		float sum1 = e + g + h;
		System.out.println(" The sum of total number:" + sum1);

		int w = 50;
		int q = 65;
		int r = 25;

		int sub = w - q - r;
		System.out.println(" The sub of total number:" + sub);

		float u = 65.5f;
		float y = 98.5f;
		float k = 70.5f;

		float sub1 = u - y - k;
		System.out.println(" The sub of total number:" + sub1);

		int t = 55;
		int i = 85;
		int o = 45;

		int mult = t * i * o;
		System.out.println(" The mult  of total number:" + mult);

		float z = 45.5f;
		float v = 55.5f;
		float n = 70.5f;

		double mult1 = z * v * n;
		System.out.println(" The mult of total number:" + mult1);

		int l = 521551;
		int m = 215;

		double divi = l / m;
		System.out.println(" The divi of total number:" + divi);

		int x = 2351465;
		int p = 55221;

		double modules = x % p;
		System.out.println(" The modules of total number:" + modules);

	}

}
